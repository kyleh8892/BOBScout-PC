    public void addMatch(){
        int[] cellData = new int[9];
        boolean[] boolData = new boolean[6];

        for(int i = 0; i < 6; i++){
            for(int dataPoint = 0; dataPoint < 9; dataPoint++){
                if(textFields.get(dataPoint + (i * 9)).getText().isBlank()){
                     cellData[dataPoint] = 0;
                }else{
                    cellData[dataPoint] = Integer.valueOf(textFields.get(dataPoint + (i * 9)).getText());
                }               
            }
            
            for(int dataPoint = 0; dataPoint < 6; dataPoint++){
                boolData[dataPoint] = radioButtons.get(dataPoint + (i * 6)).isSelected();
            }
            
            if(teamNumberFields.get(i).getText().isBlank()){
                MessageLabel.setText("Please enter a value for all team numbers!");
                break;
            }else if(MatchNumber.getText().isBlank()){
                MessageLabel.setText("Please enter a value for match number!");
                break;
            }else{
                try{
                    TeamManager.getTeamList().get(TeamManager.getTeamNumbers().indexOf(Integer.valueOf(teamNumberFields.get(i).getText()))).addMatch(Integer.valueOf(MatchNumber.getText()), cellData, boolData);
                }catch(NumberFormatException ex){                   
                    MessageLabel.setText("Please only enter numbers for team number and match number fields!");
                }catch(IndexOutOfBoundsException ex){
                    MessageLabel.setText("\"" + teamNumberFields.get(i).getText() + "\" is not a valid team!");
                }
            }
        }
        this.dispose();
    }
    
